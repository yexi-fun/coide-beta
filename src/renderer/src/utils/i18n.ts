function translate(
  language: AppLanguage,
  key: string,
  vars?: Record<string, string | number>
): string {
  const safeLanguage = language === 'en' ? 'en' : 'zh'
  const value = translations[safeLanguage][key] ?? translations.en[key] ?? key
  if (typeof value === 'function') {
    return value(vars)
  }
  return value.replace(/\{(\w+)\}/g, (_, name: string) => String(vars?.[name] ?? ''))
}

export function getLocale(language: AppLanguage): string {
  return language === 'en' ? 'en-US' : 'zh-CN'
}

export function useI18n(): {
  language: AppLanguage
  isZh: boolean
  locale: string
  setLanguage: (language: AppLanguage) => void
  t: (key: string, vars?: Record<string, string | number>) => string
} {
  const language = useSettingsStore((s) => s.language)
  const updateSettings = useSettingsStore((s) => s.updateSettings)
  const safeLanguage: AppLanguage = language === 'en' ? 'en' : 'zh'

  return {
    language: safeLanguage,
    isZh: safeLanguage === 'zh',
    locale: getLocale(safeLanguage),
    setLanguage: (nextLanguage) => updateSettings({ language: nextLanguage }),
    t: (key, vars) => translate(safeLanguage, key, vars)
  }
}
